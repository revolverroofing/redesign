export function projectLatLng(
  lat: number,
  lng: number,
  width: number,
  height: number,
) {
  const { west, east, south, north } = REGION_BOUNDS;
  const x = ((lng - west) / (east - west)) * width;
  const y = ((north - lat) / (north - south)) * height;
  return { x, y };
}

